# Área de un rectángulo
# Pide al usuario que introduzca la base y la altura de un rectángulo, y muestra el área calculada.
# Ejemplo:
# Entrada:
# Base: 5
# Altura: 3
# Salida:
# El área del rectángulo es: 15

print('Calcular el área de un rectángulo.')
#variables
base = float(input('Base: '))
altura = float(input('Altura: '))
conversion = base * altura

print(f'El área del rectángulo es: {conversion}')